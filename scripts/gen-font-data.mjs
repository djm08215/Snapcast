import { readFileSync, writeFileSync, mkdirSync } from "fs";

const regular = readFileSync("./public/fonts/NanumGothic-Regular.ttf").toString("base64");
const bold = readFileSync("./public/fonts/NanumGothic-Bold.ttf").toString("base64");

mkdirSync("./lib", { recursive: true });
writeFileSync(
  "./lib/fontData.ts",
  `// Auto-generated by scripts/gen-font-data.mjs — do not edit
export const nanumGothicRegular = "data:font/truetype;base64,${regular}";
export const nanumGothicBold = "data:font/truetype;base64,${bold}";
`
);

console.log("Font data generated: lib/fontData.ts");
